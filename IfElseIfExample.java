class IfElseIfExample{

	
	public static void main(String args[]){
	int a=60,b=20;
	if(a>60)
	{
	System.out.println("Less than 60");
	}
	else if(a==50 && b==10){
	System.out.println("a=50 and b=10");
	
	}
	else if(a==50 || b==10){
	System.out.println("a=50 OR b=10");
	
	}
	else
	{
	System.out.println("Different");
	}
	}


}